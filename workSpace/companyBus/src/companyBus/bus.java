package companyBus;

public class bus {
	
	private int number ;
	private int seters ;
	
	public bus(int number , int seters)
	{
		this.number = number ;
		this.seters = seters ;
	}
	
	public void setNumber(int seters)
	{
		this.seters = seters ;
	}

	public void setSeters(int seters)
	{
		this.seters = seters ;
	}
	
	public int getnumber()
	{
		return this.number;
	}
	
	public int getSeters()
	{
		return this.seters;
	}
	
	
	
	
	
	
	
	
	
	
}

